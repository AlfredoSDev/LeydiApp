    public GestionarInventario() {
        initComponents();
        this.setLocationRelativeTo(null);

        /*Metodos para crear y llenar tabla*/
        CrearTabla();
        modelo.setRowCount(0);
        ActualizarTabla();

        /*Controlar los clicks con el mouse en la tabla*/
        TABLA_INVENTARIO.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_poin = TABLA_INVENTARIO.rowAtPoint(e.getPoint());

                if (fila_poin > -1) {

                    productoSelecionadoActual = (String) modelo.getValueAt(fila_poin, 0);
                    EditarProducto getEditarProducto = new EditarProducto();
                    getEditarProducto.setVisible(true);

                }
                modelo.setRowCount(0);
                ActualizarTabla();
            }
        });

    }

