    public void CrearTabla() {
        TABLA_INVENTARIO = new JTable(modelo);
        SCROLL_DE_TABLA.setViewportView(TABLA_INVENTARIO);

        modelo.addColumn("Producto");
        modelo.addColumn("Cantidad");
    }

    public void ActualizarTabla() {
        try {
            Connection cn = Conexion.conetar();
            PreparedStatement pst = cn.prepareStatement(
                    "select nombre, cantidad from inventario order by nombre");

            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                Object[] fila = new Object[2];

                for (int i = 0; i < 2; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                modelo.addRow(fila);
            }

            cn.close();

        } catch (SQLException e) {
            System.err.println("ERROR AL LLENAR LA TABLA:\n" + e);
            JOptionPane.showMessageDialog(null, "Error, contacte con el desarrollador del software");
        }
    }

